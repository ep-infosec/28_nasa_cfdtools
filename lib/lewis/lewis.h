	double a[7][3];           /* Thermodynamic constants             */
	double b[2][3];           /* Thermodynamic constants             */
	Comp comp[3] ;            /* Elemental conservation constants    */
	double t_min ;            /* species minimum allowed temperature */
	double t_max ;            /* species maximum allowed temperature */
	double unused_3  ;        /* --- reserved for future use ---     */
	double mass_fr;           /* Freestream mass fraction            */
	double mole_fr;           /* Mole fraction                       */
	double mole_kg_mix;       /* Mixture mole per kilogram           */
	double cp;                /* Specific heat at constant pressure  */
	double enthalpy;          /* Species enthalpy, joules/kg.        */
	double entropy;           /* Species entropy, joules/(kg-mole)   */
	double gibbs_free ;       /* Gibbs free energy                   */ 
	double q_int ;            /* Internal partition function         */ 
	double phi ;              /* Gurvich combined partition function */ 
	int n_combo_pnt ;         /* Number of combination pointers      */
	int combo_species[10] ;   /* Listed combination visc. species    */  
